    private void loadSP() {
        defaultTableModel1.setRowCount(0);
        int stt = 1;
        for (Thongke x : repository.getsp()) {
            defaultTableModel1.addRow(new Object[]{
                stt,
                x.getChiTietSP(),
                x.getSoSP(),
                x.getLoinhuan()
            });
            stt++;
        }
    }
    private void loadNV() {
        defaultTableModel2.setRowCount(0);
        int stt=1;
        for (Thongke x : repository.getnv()) {
            defaultTableModel2.addRow(new Object[]{
                stt,
                x.getUsers(),
                x.getSoSP()
            });
            stt++;
        }
    }


